  def build_inventory
    subsystem_map = {}
    SUBSYSTEMS.each do |definition|
      subsystem_map[definition.fetch(:key)] = definition.merge(
        models_inventory: [],
        controllers_inventory: [],
        concerns_inventory: [],
        outbound_relationships: [],
        inbound_references: []
      )
    end

    model_concerns, controller_concerns = concern_constants
    models = collect_models
    controllers = collect_controllers

    model_lookup = models.index_by { |entry| entry.fetch(:name) }
    controller_lookup = controllers.index_by { |entry| entry.fetch(:name) }

    models.each do |model_entry|
      subsystem = subsystem_map.fetch(subsystem_for_model(model_entry.fetch(:name)).fetch(:key))
      subsystem[:models_inventory] << enrich_model_entry(model_entry, model_lookup, model_concerns)
    end

    controllers.each do |controller_entry|
      subsystem = subsystem_map.fetch(subsystem_for_controller(controller_entry.fetch(:name)).fetch(:key))
      subsystem[:controllers_inventory] << enrich_controller_entry(controller_entry, controller_lookup, controller_concerns)
    end

    subsystem_map.each_value do |subsystem|
      collect_subsystem_edges!(subsystem, subsystem_map)
      collect_concern_inventory!(subsystem)
      subsystem[:models_inventory].sort_by! { |entry| entry.fetch(:label) }
      subsystem[:controllers_inventory].sort_by! { |entry| entry.fetch(:label) }
      subsystem[:concerns_inventory].sort_by! { |entry| entry.fetch(:name) }
      subsystem[:outbound_relationships].uniq! { |edge| [edge[:from], edge[:to], edge[:label]] }
      subsystem[:inbound_references].uniq! { |edge| [edge[:from], edge[:to], edge[:label]] }
    end

    {
      generated_at: Time.current.iso8601,
      subsystems: subsystem_map.values
    }
  end

  def concern_constants
    [
      collect_modules(PROJECT_ROOT.join('app/models/concerns/better_together')),
      collect_modules(PROJECT_ROOT.join('app/controllers/concerns/better_together'))
    ]
  end

  def collect_modules(root)
    Dir[root.join('**/*.rb')].filter_map do |path|
      constant_name = constant_name_for_path(path, root)
      constant_name.safe_constantize
    end.compact
  end

  def collect_models
    ActiveRecord::Base.descendants.filter_map do |klass|
      next unless tracked_class?(klass)
      next if klass.abstract_class?

      {
        name: klass.name,
        label: klass.name.delete_prefix('BetterTogether::'),
        source_path: relative_source_path(klass),
        table_name: klass.table_name,
        columns: interesting_columns(klass),
        validations: validators_for(klass),
        belongs_to: belongs_to_relationships(klass),
        polymorphic: polymorphic_relationships(klass),
        translated_attributes: translated_attributes_for(klass)
      }
    end
  end

  def collect_controllers
    ActionController::Base.descendants.filter_map do |klass|
      next unless tracked_class?(klass)
      next unless relative_source_path(klass)&.start_with?('app/controllers/')

      {
        name: klass.name,
        label: klass.name.delete_prefix('BetterTogether::'),
        source_path: relative_source_path(klass),
        superclass: klass.superclass&.name,
        inferred_models: infer_controller_models(klass)
      }
    end
  end

  def tracked_class?(klass)
    klass.name&.start_with?('BetterTogether::')
  end

  def relative_source_path(klass)
    source = Object.const_source_location(klass.name)&.first
    return unless source

    Pathname.new(source).relative_path_from(PROJECT_ROOT).to_s
  rescue ArgumentError
    source
  end

  def interesting_columns(klass)
    klass.columns
         .reject { |column| NOISY_COLUMNS.include?(column.name) }
         .sort_by { |column| column_priority(column) }
         .first(10)
         .map do |column|
      [column.name, "#{column.type}#{' FK' if column.name.end_with?('_id')}"]
    end
  end

  def column_priority(column)
    [
      column.name.end_with?('_id') ? 0 : 1,
      %i[string text boolean integer datetime json jsonb].index(column.type) || 9,
      column.name
    ]
  end

  def validators_for(klass)
    klass.validators.filter_map do |validator|
      name = validator.class.name.demodulize
      next if NOISY_VALIDATORS.include?(name)

      options = validator.options.except(:class)
      summary = "#{name}: #{validator.attributes.join(', ')}"
      summary << " (#{format_validator_options(options)})" if options.present?
      summary
    end.uniq.first(8)
  end

  def format_validator_options(options)
    options.map do |key, value|
      rendered = value.is_a?(Proc) ? 'dynamic' : value.inspect
      "#{key}=#{rendered}"
    end.join(', ')
  end

  def translated_attributes_for(klass)
    attrs =
      if klass.respond_to?(:mobility_attributes)
        Array(klass.mobility_attributes).map(&:to_s)
      elsif klass.respond_to?(:translated_attribute_names)
        Array(klass.translated_attribute_names).map(&:to_s)
      else
        []
      end
    attrs.sort
  end

  def belongs_to_relationships(klass)
    klass.reflect_on_all_associations(:belongs_to).filter_map do |association|
      next if association.options[:polymorphic]

      target_name = association.klass&.name || normalize_model_name(association.class_name)
      next unless target_name

      {
        name: association.name.to_s,
        foreign_key: association.foreign_key.to_s,
        target: target_name,
        optional: association.options[:optional] == true
      }
    end
  end

  def polymorphic_relationships(klass)
    direct = klass.reflect_on_all_associations(:belongs_to).filter_map do |association|
      next unless association.options[:polymorphic]

      {
        direction: 'belongs_to',
        name: association.name.to_s,
        foreign_key: association.foreign_key.to_s,
        note: 'Polymorphic target set at runtime; foreign-key integrity is not enforced by the database.'
      }
    end

    inverse = klass.reflect_on_all_associations.filter_map do |association|
      next unless association.options[:as]

      {
        direction: association.macro.to_s,
        name: association.name.to_s,
        as: association.options[:as].to_s,
        note: 'Acts as a polymorphic owner surface; dependent classes point back through an interface column pair.'
      }
    end

    direct + inverse
  end

  def infer_controller_models(klass)
    candidates = []
    short_name = klass.name.delete_prefix('BetterTogether::').delete_suffix('Controller')
    candidates << "BetterTogether::#{short_name.singularize}" unless short_name.blank?

    if klass.name.include?('::')
      namespace_parts = klass.name.delete_prefix('BetterTogether::').delete_suffix('Controller').split('::')
      leaf = namespace_parts.pop
      namespace = namespace_parts.join('::')
      candidates << "BetterTogether::#{namespace}::#{leaf.singularize}" if namespace.present?
    end

    candidates.uniq.filter_map do |candidate|
      candidate.safe_constantize&.name
    end
  end

  def enrich_model_entry(entry, model_lookup, concern_constants)
    klass = entry.fetch(:name).constantize
    included_concerns = included_concerns_for(klass, concern_constants)
    entry.merge(
      concerns: included_concerns,
      concern_notes: included_concerns.filter_map { |concern_name| concern_configuration_for(klass, concern_name) },
      cross_subsystem_targets: entry.fetch(:belongs_to).filter_map do |association|
        target_name = association.fetch(:target)
        next unless model_lookup.key?(target_name)

        target_subsystem = subsystem_for_model(target_name).fetch(:key)
        next if target_subsystem == subsystem_for_model(entry.fetch(:name)).fetch(:key)

        association.merge(target_subsystem:)
      end
    )
  end

  def enrich_controller_entry(entry, _controller_lookup, concern_constants)
    klass = entry.fetch(:name).constantize
    included_concerns = included_concerns_for(klass, concern_constants)
    entry.merge(
      concerns: included_concerns,
      inferred_models: entry.fetch(:inferred_models).sort
    )
  end

  def included_concerns_for(klass, concern_constants)
    concern_constants.filter_map do |concern|
      concern_name = concern.name
      next unless concern_name
      next unless klass.ancestors.include?(concern)

      concern_name
    end.sort
  end

  def concern_configuration_for(klass, concern_name)
    definition = CONCERN_CAPABILITIES[concern_name]
    return unless definition&.config_extractor

    configuration = definition.config_extractor.call(klass)
    return if configuration.blank?

    "#{concern_name.delete_prefix('BetterTogether::')}: #{configuration}"
  end

  def subsystem_for_model(model_name)
    SUBSYSTEMS.find do |definition|
      Array(definition[:models]).include?(model_name) ||
        Array(definition[:model_prefixes]).any? { |prefix| model_name.start_with?(prefix) }
    end || fallback_subsystem(model_name, 'model')
  end

  def subsystem_for_controller(controller_name)
    SUBSYSTEMS.find do |definition|
      Array(definition[:controllers]).include?(controller_name) ||
        Array(definition[:controller_prefixes]).any? { |prefix| controller_name.start_with?(prefix) }
    end || fallback_subsystem(controller_name, 'controller')
  end

  def fallback_subsystem(name, kind)
    key = 'shared_supporting'
    fallback = SUBSYSTEMS.find { |definition| definition[:key] == key }
    return fallback if fallback

    raise "No subsystem mapping for #{kind} #{name.inspect}; add it to SUBSYSTEMS before exporting diagrams."
  end

  def collect_subsystem_edges!(subsystem, subsystem_map)
    subsystem[:models_inventory].each do |model_entry|
      model_entry.fetch(:cross_subsystem_targets).each do |association|
        subsystem[:outbound_relationships] << {
          from: model_entry.fetch(:name),
          to: association.fetch(:target),
          label: "#{association.fetch(:name)} (#{association.fetch(:foreign_key)})",
          target_subsystem: association.fetch(:target_subsystem)
        }
      end

      model_entry.fetch(:cross_subsystem_targets).each do |association|
        target_subsystem = subsystem_map.fetch(association.fetch(:target_subsystem))
        target_subsystem[:inbound_references] << {
          from: model_entry.fetch(:name),
          to: association.fetch(:target),
          label: "#{association.fetch(:name)} (#{association.fetch(:foreign_key)})",
          source_subsystem: subsystem.fetch(:key)
        }
      end
    end
  end

  def collect_concern_inventory!(subsystem)
    seen = Set.new

    subsystem[:models_inventory].each do |model_entry|
      model_entry.fetch(:concerns).each do |concern_name|
        next unless seen.add?([concern_name, 'model'])

        subsystem[:concerns_inventory] << concern_inventory_for(concern_name, subsystem[:models_inventory], :models_inventory)
      end
    end

    subsystem[:controllers_inventory].each do |controller_entry|
      controller_entry.fetch(:concerns).each do |concern_name|
        next unless seen.add?([concern_name, 'controller'])

        subsystem[:concerns_inventory] << concern_inventory_for(concern_name, subsystem[:controllers_inventory], :controllers_inventory)
      end
    end
  end

  def concern_inventory_for(concern_name, entries, _kind)
    definition = CONCERN_CAPABILITIES[concern_name] || ConcernCapability.new(
      capability: concern_name.demodulize.tr('_', ' ').humanize,
      risk: 'Review the concern source for configuration-specific coupling or visibility implications.'
    )

    {
      name: concern_name,
      label: concern_name.delete_prefix('BetterTogether::'),
      capability: definition.capability,
      risk: definition.risk,
      included_by: entries.filter_map do |entry|
        entry.fetch(:label) if entry.fetch(:concerns).include?(concern_name)
      end
    }
  end

  def write_inventory_exports(inventory)
    reference_root = PROJECT_ROOT.join('docs/diagrams/reference')
    FileUtils.mkdir_p(reference_root)

    File.write(reference_root.join('ce_subsystem_inventory.json'), JSON.pretty_generate(inventory))
    File.write(reference_root.join('ce_subsystem_inventory.md'), inventory_markdown(inventory))
    File.write(reference_root.join('ce_concern_capability_assessment.md'), concern_markdown(inventory))
  end

  def write_mermaid_exports(inventory)
    source_root = PROJECT_ROOT.join('docs/diagrams/source')
    FileUtils.mkdir_p(source_root)

    inventory.fetch(:subsystems).each do |subsystem|
      File.write(source_root.join("#{subsystem.fetch(:prefix)}_schema_erd.mmd"), erd_mermaid(subsystem))
      File.write(source_root.join("#{subsystem.fetch(:prefix)}_rails_layers.mmd"), layers_mermaid(subsystem))
    end

    File.write(source_root.join('ce_subsystem_interaction_map.mmd'), interaction_map_mermaid(inventory))
    File.write(source_root.join('ce_polymorphic_association_map.mmd'), polymorphic_map_mermaid(inventory))
  end

  def inventory_markdown(inventory)
    lines = [GENERATED_HEADER.chomp, '# Community Engine subsystem inventory', '']
    lines << "Generated from live Rails reflections at `#{inventory.fetch(:generated_at)}`."
    lines << ''

    inventory.fetch(:subsystems).each do |subsystem|
      lines << "## #{subsystem.fetch(:title)}"
      lines << ''
      lines << subsystem.fetch(:description)
      lines << ''
      lines << "- Mermaid ERD: `docs/diagrams/source/#{subsystem.fetch(:prefix)}_schema_erd.mmd`"
      lines << "- Mermaid Rails layers: `docs/diagrams/source/#{subsystem.fetch(:prefix)}_rails_layers.mmd`"
      lines << ''
      lines << '### Models'
      lines << ''
      lines << '| Model | Table | Key attributes | Validations | Concerns | Cross-subsystem references |'
      lines << '| --- | --- | --- | --- | --- | --- |'
      subsystem.fetch(:models_inventory).each do |model_entry|
        lines << [
          model_entry.fetch(:label),
          "`#{model_entry.fetch(:table_name)}`",
          truncate_join(model_entry.fetch(:columns).map { |name, type| "`#{name}` (#{type})" }),
          truncate_join(model_entry.fetch(:validations)),
          truncate_join(model_entry.fetch(:concerns).map { |name| "`#{name.delete_prefix('BetterTogether::')}`" }),
          truncate_join(model_entry.fetch(:cross_subsystem_targets).map do |edge|
            "`#{edge.fetch(:name)}` -> `#{edge.fetch(:target).delete_prefix('BetterTogether::')}` (#{edge.fetch(:target_subsystem)})"
          end)
        ].map { |cell| cell.presence || '—' }.then { |cells| "| #{cells.join(' | ')} |" }
      end
      lines << ''
      lines << '### Controllers'
      lines << ''
      lines << '| Controller | Inherits from | Inferred models | Controller concerns |'
      lines << '| --- | --- | --- | --- |'
      subsystem.fetch(:controllers_inventory).each do |controller_entry|
        lines << [
          controller_entry.fetch(:label),
          "`#{controller_entry.fetch(:superclass)}`",
          truncate_join(controller_entry.fetch(:inferred_models).map { |name| "`#{name.delete_prefix('BetterTogether::')}`" }),
          truncate_join(controller_entry.fetch(:concerns).map { |name| "`#{name.delete_prefix('BetterTogether::')}`" })
        ].map { |cell| cell.presence || '—' }.then { |cells| "| #{cells.join(' | ')} |" }
      end
      lines << ''
      lines << '### Polymorphic / weak-reference surfaces'
      lines << ''
      subsystem_polymorphic = subsystem.fetch(:models_inventory).filter_map do |model_entry|
        next if model_entry.fetch(:polymorphic).empty?

        "- `#{model_entry.fetch(:label)}`: #{model_entry.fetch(:polymorphic).map { |edge| "`#{edge[:name]}` (#{edge[:direction]})" }.join(', ')}"
      end
      lines.concat(subsystem_polymorphic.presence || ['- None detected from direct reflections.'])
      lines << ''
      lines << '### Concern notes'
      lines << ''
      concern_notes = subsystem.fetch(:models_inventory).flat_map { |entry| entry.fetch(:concern_notes) }
      lines.concat(concern_notes.presence&.map { |note| "- #{note}" } || ['- No additional live concern configuration notes were extracted for this subsystem.'])
      lines << ''
    end

    lines.join("\n")
  end

  def concern_markdown(inventory)
    lines = [GENERATED_HEADER.chomp, '# Community Engine concern capability assessment', '']
    lines << 'This assessment is generated from the current live class set and the concern capability notes encoded in `scripts/export_subsystem_diagrams.rb`.'
    lines << ''
    lines << '| Concern | Capability | Included by live classes | Drawbacks / risks |'
    lines << '| --- | --- | --- | --- |'

    inventory.fetch(:subsystems)
             .flat_map { |subsystem| subsystem.fetch(:concerns_inventory) }
             .uniq { |entry| entry.fetch(:name) }
             .sort_by { |entry| entry.fetch(:label) }
             .each do |entry|
      lines << [
        "`#{entry.fetch(:label)}`",
        entry.fetch(:capability),
        truncate_join(entry.fetch(:included_by).map { |name| "`#{name}`" }, separator: ', '),
        entry.fetch(:risk)
      ].then { |cells| "| #{cells.join(' | ')} |" }
    end

    lines.join("\n")
  end

  def erd_mermaid(subsystem)
    lines = ['%% Generated by scripts/export_subsystem_diagrams.rb', 'erDiagram']

    subsystem.fetch(:models_inventory).each do |model_entry|
      entity_name = erd_entity_name(model_entry.fetch(:table_name))
      model_entry.fetch(:belongs_to).each do |association|
        target_name = association.fetch(:target)
        target_entity = if target_name.start_with?('BetterTogether::') && tracked_model_name?(target_name)
                          target_subsystem = subsystem_for_model(target_name)
                          if target_subsystem.fetch(:key) == subsystem.fetch(:key)
                            erd_entity_name(model_entity_for(target_name).fetch(:table_name))
                          else
                            external_erd_entity_name(target_subsystem.fetch(:key), target_name)
                          end
                        else
                          generic_external_erd_entity_name(target_name)
                        end
        relation = association.fetch(:optional) ? 'o|--o{' : '||--o{'
        lines << "  #{target_entity} #{relation} #{entity_name} : \"#{association.fetch(:name)} / #{association.fetch(:foreign_key)}\""
      end
    end

    lines << ''
    subsystem.fetch(:models_inventory).each do |model_entry|
      lines.concat(erd_entity_block(model_entry))
      lines << ''
    end

    external_targets = subsystem.fetch(:outbound_relationships).group_by { |edge| [edge.fetch(:target_subsystem), edge.fetch(:to)] }
    external_targets.each do |(target_subsystem_key, target_name), _edges|
      lines.concat(external_entity_block(target_subsystem_key, target_name))
      lines << ''
    end

    subsystem.fetch(:models_inventory)
             .flat_map { |entry| entry.fetch(:belongs_to).map { |association| association.fetch(:target) } }
             .uniq
             .reject { |target_name| target_name.start_with?('BetterTogether::') && tracked_model_name?(target_name) }
             .each do |target_name|
      lines.concat(generic_external_entity_block(target_name))
      lines << ''
    end

    lines.join("\n")
  end

  def erd_entity_block(model_entry)
    lines = ["  #{erd_entity_name(model_entry.fetch(:table_name))} {"]
    model_entry.fetch(:columns).each do |name, type|
      lines << "    #{mermaid_type(type)} #{name}"
    end
    lines << '  }'
    lines
  end

  def external_entity_block(target_subsystem_key, target_name)
    [
      "  #{external_erd_entity_name(target_subsystem_key, target_name)} {",
      '    string export_ref',
      '  }'
    ]
  end

  def mermaid_type(type)
    type.to_s.split.first
  end

  def layers_mermaid(subsystem)
    lines = ['%% Generated by scripts/export_subsystem_diagrams.rb', 'classDiagram', '  direction LR', '']
    append_namespace(lines, 'Controllers') do
      subsystem.fetch(:controllers_inventory).map do |controller_entry|
        class_block(
          safe_id(controller_entry.fetch(:name)),
          controller_entry.fetch(:label),
          [
            '<<Controller>>',
            "source: #{controller_entry.fetch(:source_path)}"
          ],
          indent: 4
        )
      end
    end
    append_namespace(lines, 'Models') do
      subsystem.fetch(:models_inventory).map do |model_entry|
        attributes = [
          '<<Model>>',
          "table: #{model_entry.fetch(:table_name)}"
        ]
        attributes.concat(model_entry.fetch(:translated_attributes).first(3).map { |attr| "translates: #{attr}" })
        class_block(safe_id(model_entry.fetch(:name)), model_entry.fetch(:label), attributes, indent: 4)
      end
    end
    append_namespace(lines, 'Concerns') do
      subsystem.fetch(:concerns_inventory).map do |concern_entry|
        class_block(
          safe_id(concern_entry.fetch(:name)),
          concern_entry.fetch(:label),
          [
            '<<Concern>>',
            concern_entry.fetch(:capability)
          ],
          indent: 4
        )
      end
    end

    external_models = subsystem.fetch(:outbound_relationships).map { |edge| edge.fetch(:to) }.uniq
    external_models.concat(
      subsystem.fetch(:models_inventory)
               .flat_map { |entry| entry.fetch(:belongs_to).map { |association| association.fetch(:target) } }
               .reject { |target_name| target_name.start_with?('BetterTogether::') && tracked_model_name?(target_name) }
    )
    external_models.uniq!
    unless external_models.empty?
      lines << '  namespace ExternalSubsystems {'
      external_models.each do |model_name|
        label =
          if model_name.start_with?('BetterTogether::') && tracked_model_name?(model_name)
            target_subsystem = subsystem_for_model(model_name)
            "#{model_name.delete_prefix('BetterTogether::')}\\n(see #{target_subsystem.fetch(:prefix)}_rails_layers)"
          else
            "#{model_name}\\n(external dependency)"
          end
        lines.concat(class_block(safe_id(model_name), label, ['<<External model>>'], indent: 4))
      end
      lines << '  }'
      lines << ''
    end

    subsystem.fetch(:controllers_inventory).each do |controller_entry|
      controller_id = safe_id(controller_entry.fetch(:name))
      controller_entry.fetch(:inferred_models).each do |model_name|
        lines << "  #{controller_id} ..> #{safe_id(model_name)} : manages"
      end
      if controller_entry.fetch(:superclass)&.start_with?('BetterTogether::')
        lines << "  #{controller_id} --|> #{safe_id(controller_entry.fetch(:superclass))}"
      end
      controller_entry.fetch(:concerns).each do |concern_name|
        lines << "  #{controller_id} ..> #{safe_id(concern_name)} : includes"
      end
    end

    subsystem.fetch(:models_inventory).each do |model_entry|
      model_id = safe_id(model_entry.fetch(:name))
      model_entry.fetch(:concerns).each do |concern_name|
        lines << "  #{model_id} ..> #{safe_id(concern_name)} : includes"
      end
      model_entry.fetch(:belongs_to).each do |association|
        lines << "  #{model_id} --> #{safe_id(association.fetch(:target))} : #{association.fetch(:name)}"
      end
      model_entry.fetch(:polymorphic).each do |edge|
        poly_id = "Poly_#{safe_id(model_entry.fetch(:name))}_#{safe_id(edge.fetch(:name))}"
        lines.concat(class_block(poly_id, "#{edge.fetch(:name)}\\n(polymorphic)", ['<<Weak link>>'], indent: 2))
        lines << "  #{model_id} ..> #{poly_id} : #{edge.fetch(:direction)}"
      end
    end

    lines.join("\n")
  end

  def append_namespace(lines, name)
    blocks = Array(yield)
    return if blocks.empty?

    lines << "  namespace #{name} {"
    blocks.each { |block_lines| lines.concat(block_lines) }
    lines << '  }'
    lines << ''
  end

  def class_block(id, label, lines, indent:)
    spacing = ' ' * indent
    [
      "#{spacing}class #{id}[\"#{label}\"] {",
      *lines.map { |line| "#{spacing}  #{line}" },
      "#{spacing}}"
    ]
  end

  def interaction_map_mermaid(inventory)
    lines = ['%% Generated by scripts/export_subsystem_diagrams.rb', 'flowchart LR']

    inventory.fetch(:subsystems).each do |subsystem|
      lines << "  #{safe_id(subsystem.fetch(:key))}[\"#{subsystem.fetch(:title)}\"]"
    end
    lines << ''

    edge_counts = Hash.new(0)
    inventory.fetch(:subsystems).each do |subsystem|
      subsystem.fetch(:outbound_relationships).each do |edge|
        key = [subsystem.fetch(:key), edge.fetch(:target_subsystem)]
        edge_counts[key] += 1
      end
    end

    edge_counts.sort.each do |(from, to), count|
      lines << "  #{safe_id(from)} -->|#{count} cross-subsystem refs| #{safe_id(to)}"
    end

    lines.join("\n")
  end

  def polymorphic_map_mermaid(inventory)
    lines = ['%% Generated by scripts/export_subsystem_diagrams.rb', 'flowchart TB']

    inventory.fetch(:subsystems).each do |subsystem|
      lines << "  subgraph #{safe_id(subsystem.fetch(:key))}[\"#{subsystem.fetch(:title)}\"]"
      subsystem.fetch(:models_inventory).each do |model_entry|
        model_id = safe_id(model_entry.fetch(:name))
        lines << "    #{model_id}[\"#{model_entry.fetch(:label)}\"]"
        model_entry.fetch(:polymorphic).each do |edge|
          poly_id = "#{model_id}_#{safe_id(edge.fetch(:name))}"
          lines << "    #{poly_id}{{\"#{edge.fetch(:name)}\\n#{edge.fetch(:direction)}\"}}"
          lines << "    #{model_id} -.-> #{poly_id}"
        end
      end
      lines << '  end'
      lines << ''
    end

    lines << '  RiskNote["Polymorphic edges are flexible integration points, but they do not have strong foreign-key enforcement. Review allowlists, dependent behavior, and authorization around every polymorphic surface."]'
    lines.join("\n")
  end

  def model_entity_for(model_name)
    @model_entity_lookup ||= collect_models.index_by { |entry| entry.fetch(:name) }
    @model_entity_lookup.fetch(model_name)
  end

  def tracked_model_name?(model_name)
    @tracked_model_names ||= collect_models.to_set { |entry| entry.fetch(:name) }
    @tracked_model_names.include?(model_name)
  end

  def normalize_model_name(name)
    return if name.blank?
    return name if name.start_with?('BetterTogether::')

    "BetterTogether::#{name}".safe_constantize&.name || name.safe_constantize&.name
  end

  def truncate_join(values, separator: '<br>')
    return if values.blank?

    values.first(6).join(separator)
  end

  def constant_name_for_path(path, root)
    relative = Pathname.new(path).relative_path_from(root).to_s
    "BetterTogether::#{relative.delete_suffix('.rb').split('/').map(&:camelize).join('::')}"
  end

  def safe_id(value)
    value.to_s.gsub(/[^A-Za-z0-9]/, '_')
  end

  def erd_entity_name(table_name)
    table_name.upcase.gsub(/[^A-Z0-9]/, '_')
  end

  def external_erd_entity_name(subsystem_key, model_name)
    "EXTERNAL_#{safe_id(subsystem_key).upcase}__#{safe_id(model_name.delete_prefix('BetterTogether::')).upcase}"
  end

  def generic_external_erd_entity_name(model_name)
    "EXTERNAL_DEP__#{safe_id(model_name).upcase}"
  end

  def generic_external_entity_block(model_name)
    [
      "  #{generic_external_erd_entity_name(model_name)} {",
      '    string external_reference',
      '  }'
    ]
  end
